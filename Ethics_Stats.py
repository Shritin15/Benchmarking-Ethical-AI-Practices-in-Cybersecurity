import pandas as pd
import matplotlib.pyplot as plt

# Load the scores.csv file
df = pd.read_csv('scores.csv')

# List of the ethical dimensions and total score
dimensions = [
    'real_time_transparency', 'explainability', 'accountability', 'human_oversight',
    'privacy', 'data_protection', 'continuous_ethics_monitoring'
]

# Calculate statistics
summary_stats = {}
for dim in dimensions:
    summary_stats[dim] = {
        'mean': df[dim].mean(),
        'median': df[dim].median(),
        'variance': df[dim].var(),
        'range': df[dim].max() - df[dim].min()
    }

# Create a DataFrame to display summary stats
stats_df = pd.DataFrame(summary_stats).T
stats_df.index.name = 'Criterion'
stats_df.reset_index(inplace=True)

print(stats_df)

